/**
 * Convert Discord markdown to HTML.
 * 
 * @param {string} text Discord message content.
 * @returns {string} HTML string.
 */
function discordMarkdownToHtml(text) {
    if(!text) return ''

    // Preserve code blocks from processing
    const codeBlocks = []
    let html = text.replace(/```(\w*)\n?([\s\S]*?)```/g, (match, lang, code) => {
        const idx = codeBlocks.length
        codeBlocks.push(`<pre class="discord-codeblock"><code>${code.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')}</code></pre>`)
        return `%%CODEBLOCK_${idx}%%`
    })

    // Preserve inline code
    const inlineCodes = []
    html = html.replace(/`([^`]+)`/g, (match, code) => {
        const idx = inlineCodes.length
        inlineCodes.push(`<code class="discord-inline-code">${code.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')}</code>`)
        return `%%INLINECODE_${idx}%%`
    })

    // Escape HTML
    html = html
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')

    // Headers (# ## ###)
    html = html.replace(/^### (.+)$/gm, '<h3 class="discord-header">$1</h3>')
    html = html.replace(/^## (.+)$/gm, '<h2 class="discord-header">$1</h2>')
    html = html.replace(/^# (.+)$/gm, '<h1 class="discord-header">$1</h1>')

    // Block quotes (&gt; text)
    html = html.replace(/^&gt; (.+)$/gm, '<div class="discord-blockquote">$1</div>')

    // Spoiler (||text||)
    html = html.replace(/\|\|(.+?)\|\|/gs, '<span class="discord-spoiler" onclick="this.classList.toggle(\'revealed\')">$1</span>')

    // Masked links [text](url)
    html = html.replace(/\[([^\]]+)\]\((https?:\/\/[^\s)]+)\)/g, '<a href="$2" class="discord-link">$1</a>')

    // Bold + Italic (***text***)
    html = html.replace(/\*\*\*(.+?)\*\*\*/gs, '<strong><em>$1</em></strong>')

    // Bold (**text**)
    html = html.replace(/\*\*(.+?)\*\*/gs, '<strong>$1</strong>')

    // Underline (__text__)
    html = html.replace(/__(.+?)__/gs, '<u>$1</u>')

    // Italic (*text* or _text_)
    html = html.replace(/\*(.+?)\*/gs, '<em>$1</em>')
    html = html.replace(/(?<!\w)_(.+?)_(?!\w)/gs, '<em>$1</em>')

    // Strikethrough (~~text~~)
    html = html.replace(/~~(.+?)~~/gs, '<del>$1</del>')

    // Unordered list (- item)
    html = html.replace(/^- (.+)$/gm, '<li class="discord-list-item">$1</li>')
    html = html.replace(/((?:<li class="discord-list-item">.*<\/li>\n?)+)/g, '<ul class="discord-list">$1</ul>')

    // Discord mentions
    html = html.replace(/&lt;@!?(\d+)&gt;/g, '<span class="discord-mention">@사용자</span>')
    html = html.replace(/&lt;@&amp;(\d+)&gt;/g, '<span class="discord-mention">@역할</span>')
    html = html.replace(/&lt;#(\d+)&gt;/g, '<span class="discord-mention">#채널</span>')

    // Discord emoji
    html = html.replace(/&lt;(a?):(\w+):(\d+)&gt;/g, (match, animated, name, id) => {
        const ext = animated ? 'gif' : 'png'
        return `<img class="discord-emoji" src="https://cdn.discordapp.com/emojis/${id}.${ext}" alt=":${name}:" title=":${name}:" />`
    })

    // Discord timestamp
    html = html.replace(/&lt;t:(\d+)(?::([tTdDfFR]))?&gt;/g, (match, ts, format) => {
        const date = new Date(parseInt(ts) * 1000)
        let formatted
        switch(format) {
            case 't': formatted = date.toLocaleTimeString('ko-KR', { hour: '2-digit', minute: '2-digit' }); break
            case 'T': formatted = date.toLocaleTimeString('ko-KR'); break
            case 'd': formatted = date.toLocaleDateString('ko-KR'); break
            case 'D': formatted = date.toLocaleDateString('ko-KR', { year: 'numeric', month: 'long', day: 'numeric' }); break
            case 'f': formatted = date.toLocaleDateString('ko-KR', { year: 'numeric', month: 'long', day: 'numeric', hour: 'numeric', minute: 'numeric' }); break
            case 'F': formatted = date.toLocaleDateString('ko-KR', { year: 'numeric', month: 'long', day: 'numeric', weekday: 'long', hour: 'numeric', minute: 'numeric' }); break
            case 'R': {
                const diff = Math.floor((Date.now() - date.getTime()) / 1000)
                if(diff < 60) formatted = `${diff}초 전`
                else if(diff < 3600) formatted = `${Math.floor(diff/60)}분 전`
                else if(diff < 86400) formatted = `${Math.floor(diff/3600)}시간 전`
                else formatted = `${Math.floor(diff/86400)}일 전`
                break
            }
            default: formatted = date.toLocaleDateString('ko-KR', { year: 'numeric', month: 'long', day: 'numeric', hour: 'numeric', minute: 'numeric' })
        }
        return `<span class="discord-timestamp">${formatted}</span>`
    })

    // URLs (standalone, not already in <a> tag)
    html = html.replace(/(?<!href="|">)(https?:\/\/[^\s<]+)/g, '<a href="$1" class="discord-link">$1</a>')

    // Line breaks
    html = html.replace(/\n/g, '<br>')

    // Restore code blocks
    codeBlocks.forEach((block, idx) => {
        html = html.replace(`%%CODEBLOCK_${idx}%%`, block)
    })

    // Restore inline codes
    inlineCodes.forEach((code, idx) => {
        html = html.replace(`%%INLINECODE_${idx}%%`, code)
    })

    return html
}

/**
 * Load news information from the Discord channel.
 */
async function loadNews(){

    return new Promise((resolve) => {
        const options = {
            hostname: 'discord.com',
            path: `/api/v10/channels/${DISCORD_CHANNEL_ID}/messages?limit=1`,
            method: 'GET',
            headers: {
                'Authorization': `Bot ${DISCORD_BOT_TOKEN}`,
                'Content-Type': 'application/json'
            }
        }

        const req = https.request(options, (res) => {
            let data = ''

            res.on('data', (chunk) => {
                data += chunk
            })

            res.on('end', () => {
                try {
                    if(res.statusCode !== 200) {
                        loggerLanding.error('Discord API error:', res.statusCode, data)
                        resolve({ articles: null })
                        return
                    }

                    const messages = JSON.parse(data)

                    if(!messages || messages.length === 0) {
                        resolve({ articles: [] })
                        return
                    }

                    const articles = messages.map(msg => {
                        // Format date
                        const date = new Date(msg.timestamp).toLocaleDateString('ko-KR', {
                            year: 'numeric',
                            month: 'long',
                            day: 'numeric',
                            hour: 'numeric',
                            minute: 'numeric'
                        })

                        // Build content HTML
                        let content = discordMarkdownToHtml(msg.content)

                        // Add attachments (images)
                        if(msg.attachments && msg.attachments.length > 0) {
                            msg.attachments.forEach(att => {
                                if(att.content_type && att.content_type.startsWith('image/')) {
                                    content += `<br><img src="${att.proxy_url}" style="max-width: 100%; border-radius: 8px; margin-top: 10px;" />`
                                } else {
                                    content += `<br><a href="${att.url}" target="_blank">${att.filename}</a>`
                                }
                            })
                        }

                        // Add embeds
                        if(msg.embeds && msg.embeds.length > 0) {
                            msg.embeds.forEach(embed => {
                                if(embed.title || embed.description) {
                                    content += '<div style="border-left: 4px solid #5865F2; padding: 8px 12px; margin-top: 10px; background: rgba(88,101,242,0.1); border-radius: 4px;">'
                                    if(embed.title) {
                                        const embedTitle = embed.url
                                            ? `<a href="${embed.url}" target="_blank"><strong>${embed.title}</strong></a>`
                                            : `<strong>${embed.title}</strong>`
                                        content += embedTitle + '<br>'
                                    }
                                    if(embed.description) {
                                        content += discordMarkdownToHtml(embed.description)
                                    }
                                    if(embed.image) {
                                        content += `<br><img src="${embed.image.proxy_url || embed.image.url}" style="max-width: 100%; border-radius: 4px; margin-top: 8px;" />`
                                    }
                                    content += '</div>'
                                }
                                if(embed.image && !embed.title && !embed.description) {
                                    content += `<br><img src="${embed.image.proxy_url || embed.image.url}" style="max-width: 100%; border-radius: 8px; margin-top: 10px;" />`
                                }
                            })
                        }

                        // Title: first line or truncated content
                        const plainText = msg.content || ''
                        const firstLine = plainText.split('\n')[0]
                        const title = firstLine.length > 60 ? firstLine.substring(0, 60) + '...' : firstLine || '공지사항'

                        // Author name
                        const author = msg.author.global_name || msg.author.username || 'Unknown'

                        // Message link
                        const link = `https://discord.com/channels/${DISCORD_GUILD_ID}/${DISCORD_CHANNEL_ID}/${msg.id}`

                        return {
                            link,
                            title,
                            date,
                            author,
                            content,
                            comments: '',
                            commentsLink: link
                        }
                    })

                    resolve({ articles })

                } catch(parseErr) {
                    loggerLanding.error('Failed to parse Discord response:', parseErr)
                    resolve({ articles: null })
                }
            })
        })

        req.on('error', (err) => {
            loggerLanding.error('Failed to load Discord news:', err)
            resolve({ articles: null })
        })

        req.setTimeout(10000, () => {
            req.destroy()
            loggerLanding.error('Discord API request timed out')
            resolve({ articles: null })
        })

        req.end()
    })
}
